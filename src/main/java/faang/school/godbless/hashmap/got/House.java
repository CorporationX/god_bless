package faang.school.godbless.hashmap.got;

public class House {
    private final String NAME;
    private final String SIGIL;

    public House(String name, String sigil) {
        this.NAME = name;
        this.SIGIL = sigil;
    }

    public String getName() {
        return NAME;
    }

    public String getSigil() {
        return SIGIL;
    }

}
