package faang.school.godbless.gmail_rich_filters;

import java.util.Arrays;
import java.util.List;
import java.util.function.Consumer;
import java.util.function.Function;
import java.util.function.Predicate;

public class Main {

    public static void main(String[] args) {

        EmailProcessor emailProcessor = new EmailProcessor();

        // Создание списка входящих писем
        List<Email> emails = Arrays.asList(
                new Email("Письмо 1", "Текст письма 1", true),
                new Email("Письмо 2", "Текст письма 2", true),
                new Email("Спам", "Текст спама", false)
        );

        // Создание фильтров, обработчиков и преобразователей
        Predicate<Email> importantFilter = Email::isImportant;
        Consumer<Email> printEmail = email -> System.out.println("Обработано письмо: " + email.getSubject());
        Function<Email, String> toUpperCase = email -> email.getBody().toUpperCase();

        Function<Email, String> formatEmailBody = email -> String.join("-", email.getBody().split(" "));

        // Обработка писем
        System.out.println(emailProcessor.processEmails(emails, importantFilter, printEmail, toUpperCase));
        System.out.println(emailProcessor.processEmails(emails, importantFilter, printEmail, formatEmailBody));
    }
}