package Thread_3.Units;

public class Unit {

    private int power;
    private int count;

    public Unit(int power, int count) {
        this.power = power;
        this.count = count;
    }

    public int getPower() {
        return power;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public void setPower(int power) {
        this.power = power;
    }

    public int getCount() {
        return count;
    }

}
